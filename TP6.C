void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    // Variables estáticas para mantener el estado entre llamadas a la función
    static int estado_canal = 0; // Estado actual del canal (0: canal 2, 1: canal 3, 2: canal 4)
    static int incremento = 1000; // Incremento de 1000 unidades por ciclo
    static int direccion = 1; // Dirección del cambio (1: aumentando, -1: disminuyendo)

    // Verificar si la interrupción proviene del temporizador 3
    if (htim->Instance == TIM3) {
        // Alternar el estado del LED en el pin PC13
        HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);

        // Cambiar el valor del CCR (duty cycle) basado en el estado del canal actual
        switch(estado_canal) {
            case 0:
                // Modificar el duty cycle del canal 2
                TIM2->CCR2 += incremento * direccion;
                // Si alcanza el máximo, cambiar la dirección a decreciente
                if (TIM2->CCR2 >= 64000) {
                    TIM2->CCR2 = 64000;
                    direccion = -1;
                // Si alcanza el mínimo, cambiar la dirección a creciente y pasar al siguiente canal
                } else if (TIM2->CCR2 <= 0) {
                    TIM2->CCR2 = 0;
                    direccion = 1;
                    estado_canal = 1; // Pasar al canal 3
                }
                break;
            case 1:
                // Modificar el duty cycle del canal 3
                TIM2->CCR3 += incremento * direccion;
                if (TIM2->CCR3 >= 64000) {
                    TIM2->CCR3 = 64000;
                    direccion = -1;
                } else if (TIM2->CCR3 <= 0) {
                    TIM2->CCR3 = 0;
                    direccion = 1;
                    estado_canal = 2; // Pasar al canal 4
                }
                break;
            case 2:
                // Modificar el duty cycle del canal 4
                TIM2->CCR4 += incremento * direccion;
                if (TIM2->CCR4 >= 64000) {
                    TIM2->CCR4 = 64000;
                    direccion = -1;
                } else if (TIM2->CCR4 <= 0) {
                    TIM2->CCR4 = 0;
                    direccion = 1;
                    estado_canal = 0; // Regresar al canal 2
                }
                break;
            default:
                // En caso de error, reiniciar el estado del canal a 0
                estado_canal = 0;
                break;
        }
    }
}
